const countryWaterIndexMap = new Map(countryWaterIndex.map((country) => [country.iso3, country]));
const countryWaterIndexNumericMap = new Map(countryWaterIndex.map((country) => [country.numericCode, country]));

export function getCountryWaterRecord(iso3) {
  return countryWaterIndexMap.get(String(iso3 || "").toUpperCase()) || null;
}

export function getCountryWaterRecordByNumericCode(numericCode) {
  return countryWaterIndexNumericMap.get(String(numericCode || "").padStart(3, "0")) || null;
}

export function getCountryHotspots(limit = 10) {
  return [...countryWaterIndex]
    .filter((country) => Number.isFinite(country.riskScore))
    .sort((left, right) => right.riskScore - left.riskScore)
    .slice(0, limit);
}
